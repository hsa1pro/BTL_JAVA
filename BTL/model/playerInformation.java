package model;

public class playerInformation {
    private String name;
    private boolean gender;

    public void setName(String name) {
        this.name = name;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public boolean getGender() {
        return gender;
    }
}
